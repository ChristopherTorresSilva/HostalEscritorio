    private void llenarCombo(){
        String productos = _main.listaProductos();
        productosData = gson.fromJson(productos, Object[].class);
        
        for (int i = 0; i < productosData.length; i++){
            String producto = gson.toJson(productosData[i]);
            JSONObject jsonObject = new JSONObject(producto);
            comboProducto.addItem(jsonObject.get("NOMBRE").toString());
        }
        
        String tipoProductos = _main.listaTipoProductos();
        tipoProductosData = gson.fromJson(tipoProductos, Object[].class);
        
        for (int i = 0; i < tipoProductosData.length; i++){
            String tipoProducto = gson.toJson(tipoProductosData[i]);
            JSONObject jsonObject = new JSONObject(tipoProducto);
            comboTipoProducto.addItem(jsonObject.get("NOMBRE").toString());
        }
        
        String familias = _main.listaFamilias();
        familiaData = gson.fromJson(familias, Object[].class);
        
        for (int i = 0; i < familiaData.length; i++){
            String familia = gson.toJson(familiaData[i]);
            JSONObject jsonObject = new JSONObject(familia);
            comboFamiliaProducto.addItem(jsonObject.get("NOMBRE").toString());
        }
    }
